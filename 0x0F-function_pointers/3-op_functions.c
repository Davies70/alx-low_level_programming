#include "calc.h"
#include <stdio.h>

/**
* op_add - func that adds
* @a: int
* @b: int
* Return: sum
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
* op_sub - func that adds
* @a: int
* @b: int
* Return: sum
*/

int op_sub(int a, int b)
{
return (a - b);
}
/**
* op_mul - func that adds
* @a: int
* @b: int
* Return: mul
*/

int op_mul(int a, int b)
{
return (a * b);
}
/**
* op_div - func that adds
* @a: int
* @b: int
* Return: div
*/

int op_div(int a, int b)
{
return (a / b);
}
/**
* op_mod - func that adds
* @a: int
* @b: int
* Return: mod
*/

int op_mod(int a, int b)
{
return (a % b);
}
